package dex;

public class Out {
	static public void   debug (String s) {
		System.err.println(s);
	}

	static public void verbose (String s) {
		System.out.println(s);
	}

	static public void err (String s) {
		System.err.println(s);
	}
	static public void out (String s) {
		System.out.println(s);
	}
}

